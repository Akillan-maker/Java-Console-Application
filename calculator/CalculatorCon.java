public class CalculatorCon {
    //operation
    static void add(double a,double b){
        double result=a+b;
        System.out.println(result);
    }
    static void add(double a,double b,double c){
        double result=a+b+c;
        System.out.println(result);
    }
    static void sub(double a,double b){
        double result=a-b;
        System.out.println(result);
    }
    static void sub(double a,double b,double c){
        double result=a-b-c;
        System.out.println(result);
    }
    static void mul(double a,double b){
        double result=a*b;
        System.out.println(result);
    }
    static void mul(double a,double b,double c){
        double result=a*b*c;
        System.out.println(result);
    }
    static void div(double a,double b){
        double result=a/b;
        System.out.println(result);
    }
    static void div(double a,double b,double c){
        double result=a/b/c;
        System.out.println(result);
    }
    static void mod(double a,double b){
        double result=a%b;
        System.out.println(result);
    }
    static void mod(double a,double b,double c){
        double result=a%b%c;
        System.out.println(result);
    }

}
